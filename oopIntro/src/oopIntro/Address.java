package oopIntro;

public class Address {
	int id;
	String country;
	String streetAddress;
	String city;
	int postalCode;
	
	public Address(int id, String country, String streetAddress, String city, int postalCode) {
		super();
		this.id = id;
		this.country = country;
		this.streetAddress = streetAddress;
		this.city = city;
		this.postalCode = postalCode;
	}
}
