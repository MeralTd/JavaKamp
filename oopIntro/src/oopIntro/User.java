package oopIntro;

public class User {
	int id;
	String firstName;
	String lastName;
	String email;
	String password;
	
	public User(int id, String firstName, String lastName, String email, String password) {
		super();
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		this.password = password;
	}
	
}
